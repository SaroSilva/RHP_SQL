SELECT * FROM SYS.ALL_TAB_COLUMNS
WHERE OWNER = 'DBAMV'
AND 
--COLUMN_NAME LIKE '%CD_PARE%'
TABLE_NAME LIKE '%PARE%'

ORDER BY 2,3
;
